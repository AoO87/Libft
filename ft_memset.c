#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)

{
	unsigned char	x;
	size_t			i;

	x = c;
	i = 0;
	while (i < n)
	{
		*((char *)str + i) = x;
		i++;
	}
	return (str);
}
