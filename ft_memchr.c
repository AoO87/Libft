#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*t;
	size_t		i;

	i = 0;
	t = s;
	while (i < n)
	{
		if (t[i] == c)
		{
			return ((char *)s);
		}
		s++;
		i++;
	}
	return (NULL);
}
